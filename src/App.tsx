function countNeighbours(grid: number[][], row: number, col: number): number {
  let count = 0;
  const directions = [
    [-1, -1], // top-left
    [-1, 0], // top
    [-1, 1], // top-right
    [0, -1], // left
    [0, 1], // right
    [1, -1], // bottom-left
    [1, 0], // bottom
    [1, 1], // bottom-right
  ];

  for (const [dx, dy] of directions) {
    const neightbourRow = row + dx;
    const neighbourCol = col + dy;

    if (
      neightbourRow >= 0 &&
      neightbourRow < grid.length &&
      neighbourCol >= 0 &&
      neighbourCol < grid[0].length
    ) {
      count += grid[neightbourRow][neighbourCol];
    }
  }
  return count;
}

function proceedGen(grid: number[][]): number[][] {
  // Create a deep copy of the grid to avoid mutating the original grid
  const newGrid = grid.map((row) => [...row]);
  // Calculate neigbours
  newGrid.map((row, rowIndex) => {
    row.map((cell, colIndex) => {
      const neighbourCount = countNeighbours(grid, rowIndex, colIndex);
      // if neighbourCount for a dead cell is exactly 3, cell becomes alive
      if (cell === 0 && neighbourCount === 3) {
        newGrid[rowIndex][colIndex] = 1;
      }
      // If neighbourCount for a live cell is 2 or 3, cell stays alive.
      else if (cell === 1 && (neighbourCount === 2 || neighbourCount === 3)) {
        newGrid[rowIndex][colIndex] = 1;
      }
      // if neighbourCount for a live cell is < 2 or > 3, cell dies
      else {
        newGrid[rowIndex][colIndex] = 0;
      }
    });
  });
  return newGrid;
}

